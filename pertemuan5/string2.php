<?php

// Menampilkan teks "Baris" dengan karakter baris baru (newline) menggunakan tanda gugus ganda
echo "Baris\nbaru <br>"; // soal 10.a

// Menampilkan teks "Baris" dengan karakter baris baru (newline) menggunakan tanda kutip tunggal
echo 'Baris\nbaru <br>'; // soal 10.b

// Menampilkan teks "Halo" dan "Dunia" dengan mengganti posisi menggunakan karakter carriage return
echo "Halo\rDunia <br>"; // soal 10.c

// Menampilkan teks "Halo" dan "Dunia" dengan mengganti posisi menggunakan karakter carriage return, menggunakan tanda kutip tunggal
echo 'Halo\rDunia <br>'; // soal 10.d

// Menampilkan teks "Halo" dan "Dunia" dengan tab antara keduanya menggunakan elemen HTML <pre>
echo "<pre>Halo\tDunia !</pre>"; // soal 10.e

// Menampilkan teks "Halo" dan "Dunia" dengan tab antara keduanya menggunakan elemen HTML <pre>, menggunakan tanda kutip tunggal
echo '<pre>Halo\tDunia !</pre>'; // soal 10.f

// Menampilkan teks "Katakanlah "Tidak pada narkoba!"" dengan menggunakan tanda kutip ganda di dalam string
echo "Katakanlah \"Tidak pada narkoba!\" <br>"; // soal 10.g

// Menampilkan teks 'Katakanlah 'Tidak pada narkoba!'' dengan menggunakan tanda kutip tunggal di dalam string
echo 'Katakanlah \'Tidak pada narkoba!\' <br>'; // soal 10.h

?>
